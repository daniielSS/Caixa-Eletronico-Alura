package br.com.daniel.model;

public class Client {
	
	public int idCliente;
	public String nomeCliente;
	public String tipoConta;
	public int numeroConta;
	public int digitoConta;
	public int saldoCliente;
	
	
	
	public int getIdCliente() {
		return idCliente;
	}
	
	public void setIdCliente(int idCliente) {
		this.idCliente = idCliente;
	}
	
	public String getNomeCliente() {
		return nomeCliente;
	}
	
	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}
	
	public String getTipoConta() {
		return tipoConta;
	}
	
	public void setTipoConta(String tipoConta) {
		this.tipoConta = tipoConta;
	}
	
	public int getNumeroConta() {
		return numeroConta;
	}
	
	public void setNumeroConta(int numeroConta) {
		this.numeroConta = numeroConta;
	}
	
	public int getDigitoConta() {
		return digitoConta;
	}
	
	public void setDigitoConta(int digitoConta) {
		this.digitoConta = digitoConta;
	}
	
	public int getSaldoCliente() {
		return saldoCliente;
	}
	
	public void setSaldoCliente(int saldoCliente) {
		this.saldoCliente = saldoCliente;
	}
	
	
	
}
